
public class Account {
	
	private int regNumber;
	private String name;
	private int balance;
	
	public String getName() {
		
		return name;
		
	}
	
	public void setName(String n) {
		
		name = n;
		
	}
	
	public int getBalance() {
		
		return balance;
		
	}
	
	public void setBalance(int b) {
		
		balance = b;
	}
	
	public int getRegNumber() {
		
		return regNumber;
	}
	
	public void setRegNumber(int rn) {
		
		regNumber = rn;
	}
}
